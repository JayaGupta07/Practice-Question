
public class SwitchCase {

	public static void main(String[] args) {
		int n= 2;
		switch(n) {
		case 1:
			System.out.println("The given no is one");
			break;
		case 2:
			System.out.println("The given no is Two");
			break;
		case 3:
			System.out.println("The given no is Three");
			break;
		default:
			System.out.println("no. is invalid");
			
			
		}

	}

}
