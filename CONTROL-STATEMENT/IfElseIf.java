
public class IfElseIf {

	public static void main(String[] args) {
		int A= 10;
		int B =20;
		if(A>B){
			System.out.println(A+" is greater than "+ B+".");
		}
		else if(A<B){
			System.out.println(B+" is greater than "+ A+".");
		}
		else{
			System.out.println(A+" is greater than "+ B+".");
		}


	}

}
